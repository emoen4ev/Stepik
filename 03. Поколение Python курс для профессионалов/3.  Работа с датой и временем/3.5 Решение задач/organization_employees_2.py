from datetime import datetime, timedelta

pattern = '%d.%m.%Y'

data = {}

current_date = datetime.strptime(input(), pattern)

start_check = current_date
end_check = current_date + timedelta(days=8)
current_year = end_check.year
is_date = False

number_employees = int(input())

for _ in range(number_employees):
    name, birthday = input().rsplit(' ', 1)
    birthday = datetime.strptime(birthday, pattern)

    data[birthday] = name

sorted_data = sorted(data.items(), reverse=True)

for current_date, current_name in sorted_data:
    current_date = current_date.replace(year=current_year)
    if start_check < current_date < end_check:
        is_date = True
        print(current_name)
        break

